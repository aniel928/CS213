package chess;

/**
* Possible names of chess pieces.
* @author alh220
* @author jmuccino
*
*/
enum PieceName
{
	/**
	 * Identifies piece as King 
	 * @see King
	 */
	KING,
	
	/**
	 * Identifies piece as Queen 
	 * @see Queen
	 */
	QUEEN,
	
	/**
	 * Identifies piece as Bishop 
	 * @see Bishop
	 */
	BISHOP,
	
	/**
	 * Identifies piece as Knight 
	 * @see Knight
	 */
	KNIGHT, 
	
	/**
	 * Identifies piece as Rook 
	 * @see Rook
	 */
	ROOK, 
	
	/**
	 * Identifies piece as Pawn 
	 * @see Pawn
	 */
	PAWN, 
	
	/**
	 * Identifies piece as GhostPawn 
	 * @see GhostPawn
	 */
	GHOST;
}


